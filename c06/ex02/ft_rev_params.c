#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int j;

	i = argc;
	while (i > 1)
	{
		j = 0;
		while (argv[i - 1][j])
			j++;
		write(1, &argv[i - 1][0], j);
		write(1, "\n", 1);
		i--;
	}
}
